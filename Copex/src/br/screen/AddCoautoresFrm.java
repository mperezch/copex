    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (cbPerson.getSelectedIndex() != 0) {
            Person escolhida = (Person) cbPerson.getSelectedItem();
            if (autor.getId() == escolhida.getId()) {
                JOptionPane.showMessageDialog(rootPane, "Essa Pessoa Ã‰ o Autor,\nEscolha outra!");
                cbPerson.requestFocus();
            } else {
                insertTable(escolhida);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Informe a Pessoa!");
            cbPerson.requestFocus();
        }

    }//GEN-LAST:event_jButton7ActionPerformed

