    private void insertTable(Person p) {
        if (p != null) {
            pessoas.add(p);
        }
        PersonTableModel ptm = new PersonTableModel(pessoas);
        tbPerson.setModel(ptm);
        FormataTamanhoColunasJTable.packColumns(tbPerson, 1);
        tbPerson.setAutoCreateRowSorter(true);
    }

    private void insertPersons() {
        cbPerson.removeAllItems();
        cbPerson.addItem("-");

        PersonDAO pDAO = new PersonDAO();
        List<Person> lista = pDAO.list("nome");
        for (int i = 0; i < lista.size(); i++) {
            cbPerson.addItem(lista.get(i));
        }
    }

