    public void insertCourse() {
        cbCourse.removeAllItems();
        cbCourse.addItem("-");
        
        CourseDAO cDAO = new CourseDAO();
        List<Course> lista = cDAO.list("descricao");
        for (int i = 0; i < lista.size(); i++) {
            cbCourse.addItem(lista.get(i));
        }
        
    }

