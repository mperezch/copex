    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (tfDescription.getText().equals("") || cbCourse.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Informe os Campos Obrigatórios(*)!");
            tfDescription.requestFocus();
            return;
        }
        Certificate c = new Certificate();
        c.setDescricao(tfDescription.getText());
        c.setCourse((Course) cbCourse.getSelectedItem());
        
        if (!tfCH.getText().equals("")) {
            c.setCh(Integer.parseInt(tfCH.getText()));
        }
        if (!tfInitialDate.getText().equals("  /  /    ")) {
            String dataString = tfInitialDate.getText();
            try {
                DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date data;
                data = new java.util.Date(fmt.parse(dataString).getTime());
                c.setData(data);
            } catch (ParseException ex) {
                System.out.println(ex.getMessage());
            }
        }
        
        setVisible(false);
        
        CertificateDAO cDAO = new CertificateDAO();
        cDAO.add(c);
    }//GEN-LAST:event_jButton2ActionPerformed

