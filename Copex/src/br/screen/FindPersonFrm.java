    public void preencheTabela(String texto) {
        PersonDAO pDAo = new PersonDAO();
        if (texto.equals("")) {
            PersonTableModel ptm = new PersonTableModel(pDAo.list());
            tbPessoas.setModel(ptm);
        } else {
            PersonTableModel ptm = new PersonTableModel(pDAo.checkExistsLike("nome", texto));
            tbPessoas.setModel(ptm);
        }

        FormataTamanhoColunasJTable.packColumns(tbPessoas, 1);
    }

