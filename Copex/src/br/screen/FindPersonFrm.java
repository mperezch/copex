    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (tbPessoas.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecioen uma linha da tabela!");
        } else {
            PersonTableModel ptm = (PersonTableModel) tbPessoas.getModel();
            Person pessoaSelecionada = ptm.getPessoa(tbPessoas.getSelectedRow());
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja Excluir a Pessoa " + pessoaSelecionada.getNome() + "?",
                    "Excluir Pessoa", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                PersonDAO pDAO = new PersonDAO();
                try {
                    pDAO.remove(pessoaSelecionada);
                } catch (Exception ex) {
                      JOptionPane.showMessageDialog(rootPane, "Pessoa não pode ser excluída! \nExistem Certificados ou Documentos cadastrados para esta Pessoa!");
                }
                preencheTabela(tfPesquisarNome.getText().toString());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (tbPessoas.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecioen uma linha da tabela!");
        } else {
            PersonTableModel ptm = (PersonTableModel) tbPessoas.getModel();
            Person pessoaSelecionada = ptm.getPessoa(tbPessoas.getSelectedRow());
            RegisterPersonFrm rpf = new RegisterPersonFrm(pessoaSelecionada);
            rpf.setVisible(true);
            preencheTabela(tfPesquisarNome.getText().toString());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

